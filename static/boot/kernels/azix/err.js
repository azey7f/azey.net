// error codes
window.EGENERIC		=-	 1; // generic failure
window.EPERM		=-	 2; // operation not permitted
window.EINVAL		=-	 3; // invalid argument
window.EBUSY		=-	 4; // target is busy
window.EBADDRV		=-	 5; // unknown filesystem type
window.EDRV		=-	 6; // filesystem doesn't support this operation
window.EROFS		=-	 7; // read-only filesystem
window.EIO		=-	 8; // input/output error
window.ENOENT		=-	 9; // file or directory does not exist
window.ENOTDIR		=-	10; // not a directory
window.EISDIR		=-	11; // not a file
window.ENOTEMPTY	=-	12; // directory not empty
window.EEXIST		=-	13; // already exists
window.EXDEV		=-	14; // path on a different filesystem
window.EBADF		=-	15; // bad file descriptor
window.EWOULDBLOCK	=-	16; // call would block
window.ENOTTY		=-	17; // file isn't a TTY
window.EPIPE		=-	18; // broken pipe

window.EIMPL		=-	99; // not implemented
